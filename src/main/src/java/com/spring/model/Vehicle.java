package com.spring.model;

import org.springframework.stereotype.Component;


public class Vehicle {
    private String name = "Toyota";

    public String getName() {
        return this.name;
    }
    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Vehicle{" +
                "name='" + name + '\'' +
                '}';
    }
}
